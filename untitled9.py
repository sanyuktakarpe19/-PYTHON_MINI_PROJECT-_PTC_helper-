def prepare_media_calculator(db):
    list_media(db)
    choice = input("Choose a medium: ").strip().upper()
    if choice not in db:
        print("Invalid medium.")
        return

    try:
        vol_ml = float(input("Enter final volume (mL): ").strip())
    except:
        print("Invalid value.")
        return

    L = vol_ml / 1000
    info = db[choice]
    salts = info["salts_gL"] * L
    sucrose = info["sucrose_gL"] * L
    agar = info["agar_gL"] * L

    print(f"\nRecipe for {vol_ml} mL of {choice}:")
    print(f" Salts: {salts:.2f} g")
    print(f" Sucrose: {sucrose:.2f} g")
    print(f" Agar: {agar:.2f} g")
    print(" Adjust pH to ~5.7 and autoclave.")

