def print_header():
    print("\n=== Plant Tissue Culture Assistant ===")

def list_media(db):
    print("\nAvailable media:")
    for name, info in db.items():
        print(f" - {name}: {info['use']}")

def view_media_info(db):
    name = input("Enter media name: ").strip()
    name = name[0].upper() + name[1:].lower()
    if name not in db:
        print("Media not found.")
        list_media(db)
        return
    info = db[name]
    print(f"\n{name} medium:")
    print(f" Use: {info['use']}")
    print(f" Salts (g/L): {info['salts_gL']}")
    print(f" Sucrose (g/L): {info['sucrose_gL']}")
    print(f" Agar (g/L): {info['agar_gL']}")

